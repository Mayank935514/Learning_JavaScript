/** Comparison Operator */
// Every comparison operator returns a boolean value

console.log(50 > 30);
console.log("2" > 1);
console.log("01" == 1);  // Double equals does not checks the type of value

/** Strict Equality Operator */
console.log("01" === 1);

console.log(null == undefined);
console.log(null === undefined);
console.log(null > 0);
console.log(null < 1);