/** Variables */
//  Variable is a placeholder which can store some data and information.

var message;
message = "Hello World!";
message = "My name is Mayank"; //Redeclaration with same variable is allowed.
console.log(message);

// Let variable //
let text;
text = "JavaScript is the best!!"; // redeclaration with same variable name will give error
console.log(text);

// const variable //
const number = 3; // Initialisation is must and can't redeclare like let variable
console.log(number);