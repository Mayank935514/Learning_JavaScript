/** DataTypes */
/** Data Types describe the different types or kinds of data that we are gonna be working with and storing in variables. */

/** String */
let userName = "Mayank";
console.log(typeof userName);

/** Number */
let age = 21;
console.log(typeof age);

/** Boolean */
let isProduct = false;
console.log(typeof isProduct);

/** Undefined */
let myName;     // if we do not initialise a variable it will give value undefined
console.log( typeof myName);

/** Null */
let noValue = null;
console.log(typeof noValue); //the type of null is object

/** Object */
const personDetails = {name: "Mayank", age: 21, education: "Engg"}
console.log(typeof personDetails);

const arrayofNumbers = [1,2,3,4,5];
console.log(typeof arrayofNumbers);